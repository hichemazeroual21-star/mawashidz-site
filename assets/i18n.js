function getMawashiLanguage() {
  const saved = localStorage.getItem('mdz_language');
  return MDZ_LANGS.includes(saved) ? saved : 'ar';
}

function t(key, vars) {
  const lang = getMawashiLanguage();
  const pack = MDZ_I18N[lang] || MDZ_I18N.ar;
  let text = pack[key] ?? MDZ_I18N.ar[key] ?? key;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      text = text.replaceAll(`{${k}}`, v);
    });
  }
  return text;
}

function applyI18n(lang) {
  const pack = MDZ_I18N[lang] || MDZ_I18N.ar;
  document.documentElement.lang = lang;
  /* ثبّت اتجاه الهيكل (RTL) حتى لا يتبدل شكل الصفحة عند اختيار EN/FR/DE */
  document.documentElement.dir = 'rtl';

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (pack[key] != null) el.textContent = pack[key];
  });

  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const key = el.dataset.i18nHtml;
    if (pack[key] != null) el.innerHTML = pack[key];
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    if (pack[key] != null) el.placeholder = pack[key];
  });

  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    const key = el.dataset.i18nAria;
    if (pack[key] != null) el.setAttribute('aria-label', pack[key]);
  });

  const titleEl = document.querySelector('title[data-i18n]');
  if (titleEl && pack[titleEl.dataset.i18n] != null) titleEl.textContent = pack[titleEl.dataset.i18n];

  const metaDesc = document.querySelector('meta[data-i18n-content]');
  if (metaDesc && pack[metaDesc.dataset.i18nContent] != null) metaDesc.setAttribute('content', pack[metaDesc.dataset.i18nContent]);

  document.body.dataset.lang = lang;

  document.querySelectorAll('[data-lang]').forEach((btn) => {
    const code = btn.dataset.lang;
    btn.classList.toggle('active', code === lang);
    btn.removeAttribute('aria-disabled');
    btn.title = pack.langName?.[code] || code.toUpperCase();
  });

  if (typeof refreshDynamicI18n === 'function') refreshDynamicI18n();
  if (typeof syncAuthHeader === 'function') syncAuthHeader();
  const versionEl = document.getElementById('siteVersionLabel');
  if (versionEl && pack.siteVersion) versionEl.textContent = t('siteVersion', { version: MDZ_APP_VERSION });
}

function setMawashiLanguage(code) {
  if (!MDZ_LANGS.includes(code)) code = 'ar';
  localStorage.setItem('mdz_language', code);
  applyI18n(code);
}

